package com.istl.samples.faceverification.utils;

public class NidRequest {
    private String nid;
    private String dob;

    public NidRequest() {
    }

    public String getNid() {
        return nid;
    }

    public void setNid(String nid) {
        this.nid = nid;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }
}
